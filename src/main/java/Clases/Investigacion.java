
package Clases;


public class Investigacion {
    public int id_investigacion;
    public int tiempo_dedicado;
    public String tema_investigado;
    public int porcentaje_logrado;
    public String dudas;

    public Investigacion(int id_investigacion, int tiempo_dedicado, String tema_investigado, int porcentaje_logrado, String dudas) {
        this.id_investigacion = id_investigacion;
        this.tiempo_dedicado = tiempo_dedicado;
        this.tema_investigado = tema_investigado;
        this.porcentaje_logrado = porcentaje_logrado;
        this.dudas = dudas;
    }
    
    
   
}

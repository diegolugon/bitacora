
package Clases;


public class Item {
    public int id_item;
    public String concepto;
    public String descripcion;
    public String duda;
    public String aprendido; 

    public Item(int id_item, String concepto, String descripcion, String duda, String aprendido) {
        this.id_item = id_item;
        this.concepto = concepto;
        this.descripcion = descripcion;
        this.duda = duda;
        this.aprendido = aprendido;
    }
    
    
}


package Clases;


public class Ejercicio {
    public int id_ejercicio;
    public String experiencia;
    public int tiempo_dedidcado;
    public String dudas;
    public int porcentaje_logrado;

    public Ejercicio(int id_ejercicio, String experiencia, int tiempo_dedidcado, String dudas, int porcentaje_logrado) {
        this.id_ejercicio = id_ejercicio;
        this.experiencia = experiencia;
        this.tiempo_dedidcado = tiempo_dedidcado;
        this.dudas = dudas;
        this.porcentaje_logrado = porcentaje_logrado;
    }
    
    
    
}
